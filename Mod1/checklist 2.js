// Below is a list of necessary technical skills in which you should be proficient by the time of the interview/admissions assessment.

// Basic Level, the student can:
// Declare a variable
// Write a function definition
// Write a function that accepts arguments
// Write a function that returns a value
// Write a function that returns a value based on passed in arguments
// Use an ordinary if /else if /else / statements
// Use math operators
// Use logical operators
// Create a string
// Concatenate strings
// Create an array
// Create an object
// Create and use a basic for loop and a for-in loop
// Create and use a while loop


// Intermediate Level, the student can:
// Use array methods correctly
// Use string methods correctly
// Use object methods correctly
// Write a function that evaluates an expression as an argument
// Offload work to a helper function
// Use logical operators inside of if statements
// Use nested if/else statements
// Understand and create tests using assertion functions

// Students should have the ability to work autonomously, quickly, and correctly, verbally representing their work accurately. A student should be able to:

// Clearly explains their reasoning
// Quickly understand and apply corrections and suggestions
// Code correctly without additional prompting
// Use time well
// Listen well
// Ask useful questions if necessary
// Display confidence i.e. not crumble under the pressure
// Make reasonable, useful decisions when faced with new content
// Fix their own bugs
// Display clear case-based reasoning